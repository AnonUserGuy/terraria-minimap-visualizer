    public static applyPaint(group: MapCellGroup, color: Color, paintId: PaintID) {
        if (paintId === PaintID.NegativePaint) {
            if (group === MapCellGroup.Wall) {
                return new Color((255 - color.r) * 0.5, (255 - color.g) * 0.5, (255 - color.b) * 0.5)
            }
            else {
                return new Color(255 - color.r, 255 - color.g, 255 - color.b)
            }
        }
        const paintColor = mapCellColors.paintColors[paintId] || Color.globalWhite;
        const r = color.r / 255;
        const g = color.g / 255;
        const b = color.b / 255;
        const factor = paintId === PaintID.ShadowPaint ? Math.min(r, g, b) * 0.3 : Math.max(r, g, b);
        return new Color(paintColor.r * factor, paintColor.g * factor, paintColor.b * factor);
    }

    public static getSkyId(y: number, worldSurface: number) {
        if (y < worldSurface) {
            return Math.floor(mapCellColors.maxSkyGradients * (y / worldSurface));
        } else {
            return mapCellColors.maxSkyGradients;
        }
    }
